/*
  # เพิ่มคอลัมน์หมายเหตุในตารางการขาย

  1. การเปลี่ยนแปลง
    - เพิ่มคอลัมน์ `note` ในตาราง `sales`
    - ประเภทข้อมูล: TEXT (สามารถเป็น NULL ได้)
    - สำหรับเก็บหมายเหตุเพิ่มเติมของการขายแต่ละรายการ

  2. หมายเหตุ
    - คอลัมน์นี้เป็น optional (สามารถเป็น NULL ได้)
    - ไม่มีค่าเริ่มต้น เพื่อให้ยืดหยุ่นในการใช้งาน
*/

-- เพิ่มคอลัมน์ note ในตาราง sales
ALTER TABLE sales 
ADD COLUMN IF NOT EXISTS note TEXT;

-- เพิ่มความคิดเห็นสำหรับคอลัมน์ใหม่
COMMENT ON COLUMN sales.note IS 'หมายเหตุเพิ่มเติมสำหรับการขายแต่ละรายการ';